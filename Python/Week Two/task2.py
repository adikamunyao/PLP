# Task 2: Tuple of favorite books and printing each one on a new line
def favorite_books():
    books = ("The Catcher in the Rye", "1984", "To Kill a Mockingbird", "Moby-Dick", "Pride and Prejudice")
    
    # Print each book name
    for book in books:
        print(book)

# Call the function
